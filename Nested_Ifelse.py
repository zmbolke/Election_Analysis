#What is your score?
score = int(input('What is you test score?'))
#If above 90 than A
if score >= 90:
    print('Your grade is an A.')
elif score >= 80:
    print('Your grade is a B.')
elif score >= 70:
    print('Your grade is a C.')
elif score >= 60:
    print('Your score is a D.')
else:
    print('Your grade is an F.') 
                   
